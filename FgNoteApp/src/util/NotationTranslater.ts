export function textToNotationImages(text: string): string[] {
    const notationImages: string[] = [];
    const regex = /(1\+2\+3\+4|[1-4](?:\+[1-4]){1,3}|UB|UF|DB|DF|SS[LR]?|BT|CH|ws|U|D|B|F|ub|uf|db|df|u|d|b|f|n|[1-4]|,|~)/g;
    const parts = text.match(regex);

    parts?.forEach((part:string) => {
        notationImages.push(notationImageMap[part] ?? "");
    })
    return notationImages;
}